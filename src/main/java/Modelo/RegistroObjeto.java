public class RegistroObjeto {
    private int Daniel;
    private String Jorge;

    public RegistroObjeto(int Daniel, String Jorge) {
        this.Daniel = Daniel;
        this.Jorge = Jorge;
    }

    public int getDaniel() {
        return Daniel;
    }

    public void setDaniel(int Daniel) {
        this.Daniel = Daniel;
    }

    public String getJorge() {
        return Jorge;
    }

    public void setJorge(String Jorge) {
        this.Jorge = Jorge;
    }
    
}

